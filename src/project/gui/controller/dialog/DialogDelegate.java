package project.gui.controller.dialog;

/**
 * Delegate fuer Rueckmeldungen ueber Nutzereingaben in Dialogen
 */
public interface DialogDelegate
{
	/**
	 * Der Nutzer hat den Dialog abgebrochen.
	 *
	 * @param dialog abgebrochener Dialog
	 */
	void dialogDidCancel(Dialog dialog);

	/**
	 * Der Nutzer hat den Dialog bestaetigt
	 * @param dialog bestaetigter Dialog
	 */
	void dialogDidReturn(Dialog dialog);
}
